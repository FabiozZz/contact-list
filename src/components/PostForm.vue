<template>
  <form @submit.prevent>
    <h4>Создание поста</h4>
    <input
      v-model="post.title"
      class="input"
      type="text"
      placeholder="название поста"
    />
    <input
      v-model="post.description"
      class="input"
      type="text"
      placeholder="описание поста"
    />
    <button class="btn" @click="createPost">Создать</button>
  </form>
</template>

<script>
export default {
  data() {
    return {
      post: {
        title: "",
        description: "",
      },
    };
  },
  methods: {
    createPost() {
      this.post.id = Date.now();
      this.$emit('create',this.post)
      this.post = {
      title: "",
      description: ""
      }
    },
  },
};
</script>

